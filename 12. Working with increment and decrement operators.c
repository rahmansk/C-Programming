//Working of increment and decrement operators

#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a = 10, b = 100;
    float c = 10.5, d = 100.5;

    printf("++a = %d \n", ++a);

    printf("--a = %d \n", --b);

    printf("++c = %d \n", ++c);

    printf("--d = %d \n", --d);

    return 0;
}
