//Escape Sequence Character

#include <stdio.h>
#include <stdlib.h>

void main()
{
    printf("Hello\n ");   //New line(line break)
    printf("Hello\b ");   //Backspace
    printf("Hello\t ");   //Horizontal Tab
    printf("Hello\f ");   //Form Feed
    printf("Hello\a ");   //Alert(alert bell)
    printf("Hello\r ");   //Carriage Return
    printf("Hello\v ");   //Vertical Tab
    printf("Hello\? ");   //Question Mark
    printf("Hello\' ");   //Single Quote
    printf("Hello\" ");   //Double Quote
    printf("Hello\\ ");   //Backslash
    printf("Hello\0 ");   //Null
}
